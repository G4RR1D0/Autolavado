public void agregarUsuarios(){
    String nombre1=jTextField1.getText();
    String primerApellido1=jTextField2.getText();
    String segundoApellido1=jTextField3.getText();
    String Direccion1=jTextField4.getText();
    String Telefono1=jTextField5.getText();
    String Correo1=jTextField6.getText();
    String Usuario1=jTextField7.getText();
    String Password1=jTextField8.getText();
    String FechaNacimiento1=jTextField9.getText();
    int idRol1=1;
    if(nombre1.isEmpty()||primerApellido1.isEmpty()||segundoApellido1.isEmpty()||Direccion1.isEmpty()||Telefono1.isEmpty()||Correo1.isEmpty()||Usuario1.isEmpty()||Password1.isEmpty()||FechaNacimiento1.isEmpty()){
        JOptionPane.showMessageDialog(this,"CamposObligatorios");
    }else{
        usuarioController.insertarUsuario(new usuarioModel(nombre1,primerApellido1,segundoApellido1,Direccion1,Telefono1,Correo1,Usuario1,Password1,FechaNacimiento1,idRol1));
        jTextField1.setText(" ");
      jTextField2.setText(" ");
      jTextField3.setText(" ");
      jTextField4.setText(" ");
      jTextField5.setText(" ");
      jTextField6.setText(" ");
      jTextField7.setText(" ");
      jTextField8.setText(" ");
      jTextField9.setText(" ");
        
        cargarUsuarios();
    }    }
   
   
